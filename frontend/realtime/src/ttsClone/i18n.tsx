export function resolveLocale(query: string, saved: string | null): Locale {
  const requested = new URLSearchParams(query).get('lang');
  for (const candidate of [requested, saved, 'en']) {
    if (candidate === 'en' || candidate === 'zh-TW' || candidate === 'ja' || candidate === 'ko') {
      return candidate;
    }
  }
  return 'en';
}

export const LocaleContext = createContext({ locale: 'en' as Locale, catalog: CATALOGS.en });
export const useCloneLocale = () => useContext(LocaleContext);
